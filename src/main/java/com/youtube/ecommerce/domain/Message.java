package com.youtube.ecommerce.domain;


public class Message {
    public String message;

    public Message(String message) {
        super();
        this.message = message;
    }

    public Message() {
        super();
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
    
    
    
    
    
}
